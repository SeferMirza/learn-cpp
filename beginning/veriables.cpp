#include <iostream>

using namespace std;

int main()
{
  // Variables
  string text = "string";
  int number = 5;
  float length = 1.75;
  char score = 'A';

  cout << "Variables" << endl;
  cout << "text: " << text << endl;
  cout << "number: " << number << endl;
  cout << "length: " << length << endl;
  cout << "score: " << score << endl;

  cout << endl;
  cout << "Operators" << endl;
  cout << "5 + 5: " << 5 + 5 << endl;
  cout << "5 * 5: " << 5 * 5 << endl;
  cout << "5 / 5:" << 5 / 5 << endl;
  cout << "5 % 5:" << 5 % 5 << endl;
}